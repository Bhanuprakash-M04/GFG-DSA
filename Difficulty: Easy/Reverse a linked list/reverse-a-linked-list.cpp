class Solution {
  public:
    Node* reverseList(Node* head) {
        if(head==NULL || head->next==NULL)
            return head;
        Node* prev=NULL;
        Node* curr=head;
        Node* tmp=head;
        while(tmp){
            tmp=tmp->next;
            curr->next=prev;
            prev=curr;
            curr=tmp;
        }
        return prev;
    }
};