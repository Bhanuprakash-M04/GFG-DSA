class Solution {
  public:
    // Function to count the number of digits in n that evenly divide n
    int evenlyDivides(int n) {
        int tmp=n;
        int cnt=0;
        while(tmp)
        {
            int digit=tmp%10;
            if(digit && n%digit==0)
                cnt++;
            tmp=tmp/10;
        }
        return cnt;
        // code here
    }
};

